    def _browse_input(self):
        path = filedialog.askopenfilename(
            title="Open STL File",
            filetypes=[("STL Files", "*.stl *.STL"), ("All Files", "*.*")])
        if not path:
            return
        self.input_path.set(path)
        if not self.output_path.get():
            self.output_path.set(os.path.splitext(path)[0] + ".scad")
        if not self.module_name.get() or self.module_name.get() == "shape":
            name = os.path.splitext(os.path.basename(path))[0]
            name = name.replace(" ", "_").replace("-", "_")
            self.module_name.set(name)

    def _browse_output(self):
        path = filedialog.asksaveasfilename(
            title="Save SCAD File",
            defaultextension=".scad",
            filetypes=[("OpenSCAD Files", "*.scad"), ("All Files", "*.*")])
        if path:
            self.output_path.set(path)

    def _load_stl(self):
        path = self.input_path.get().strip()
        if not path:
            messagebox.showwarning("No File", "Please select an STL input file first.")
            return
        if not os.path.isfile(path):
            messagebox.showerror("Not Found", f"File not found:\n{path}")
            return

        self._btn_load.config(state="disabled")
        self._btn_convert.config(state="disabled")
        self._set_status("Loading STL...", C["warn"])
        self._log_msg(f"Loading: {path}", "info")

        def _do():
            try:
                m, uv, faces, stats = load_stl(path)
                self.after(0, lambda: self._on_loaded(m, uv, faces, stats))
            except Exception as ex:
                self.after(0, lambda e=ex: self._on_load_error(e))

        threading.Thread(target=_do, daemon=True).start()

    def _on_loaded(self, m, uv, faces, stats):
        self._mesh_obj     = m
        self._unique_verts = uv
        self._faces        = faces
        self._stats        = stats

        sz = stats["size"]
        updates = {
            "Triangles": f"{stats['triangles']:,}",
            "Vertices":  f"{stats['unique_verts']:,}",
            "Size X":    f"{sz['x']:.3f}",
            "Size Y":    f"{sz['y']:.3f}",
            "Size Z":    f"{sz['z']:.3f}",
            "File KB":   f"{stats['file_size_kb']:.1f}",
        }
        for k, v in updates.items():
            self._info_labels[k].config(text=v)

        self._btn_load.config(state="normal")
        self._btn_convert.config(state="normal")
        self._set_status(
            f"Loaded - {stats['triangles']:,} triangles, {stats['unique_verts']:,} vertices",
            C["accent2"])
        self._log_msg(
            f"OK  triangles={stats['triangles']:,}  verts={stats['unique_verts']:,}", "ok")

        self._render_preview()

    def _on_load_error(self, ex):
        self._btn_load.config(state="normal")
        self._set_status(f"Load failed: {ex}", C["error"])
        self._log_msg(f"ERROR: {ex}", "error")
        messagebox.showerror("Load Error", str(ex))

    def _render_preview(self):
        if not HAS_MPL or self._unique_verts is None:
            return

        self._ax.cla()
        self._style_axes()

        faces = self._faces
        MAX_PREVIEW_FACES = 5000
        sampled = False
        if len(faces) > MAX_PREVIEW_FACES:
            idx = np.random.choice(len(faces), MAX_PREVIEW_FACES, replace=False)
            faces = faces[idx]
            sampled = True

        tris = self._unique_verts[faces]

        if self._preview_mesh_style == "solid":
            poly = Poly3DCollection(tris, alpha=0.85,
                                    facecolor="#1e3a5f", edgecolor="#2d5a8e",
                                    linewidth=0.1)
        else:
            poly = Poly3DCollection(tris, alpha=0.0,
                                    edgecolor=C["accent"], linewidth=0.3)

        self._ax.add_collection3d(poly)

        all_pts = self._unique_verts.flatten()
        self._ax.auto_scale_xyz(all_pts, all_pts, all_pts)
        self._ax.view_init(elev=self._preview_angle[0], azim=self._preview_angle[1])

        if sampled:
            self._ax.set_title(
                f"Preview: {MAX_PREVIEW_FACES:,} of {len(self._faces):,} faces",
                color=C["text_dim"], fontsize=8, pad=4)

        self._canvas.draw_idle()

    def _set_style(self, style):
        self._preview_mesh_style = style
        self._render_preview()

    def _reset_view(self):
        self._preview_angle = [30, -45]
        self._render_preview()

    def _on_mouse_press(self, event):
        if event.button == 1:
            self._drag_start = (event.x, event.y, *self._preview_angle)

    def _on_mouse_release(self, event):
        self._drag_start = None

    def _on_mouse_drag(self, event):
        if self._drag_start is None:
            return
        dx = event.x - self._drag_start[0]
        dy = event.y - self._drag_start[1]
        self._preview_angle[0] = self._drag_start[2] - dy * 0.5
        self._preview_angle[1] = self._drag_start[3] + dx * 0.5
        if self._unique_verts is not None:
            now = time.time()
            if now - self._last_drag_draw < 0.03:
                return
            self._last_drag_draw = now
            self._ax.view_init(elev=self._preview_angle[0], azim=self._preview_angle[1])
            self._canvas.draw_idle()

    def _on_scroll(self, event):
        if self._unique_verts is None:
            return
        factor = 0.9 if event.button == "up" else 1.1
        xlim = self._ax.get_xlim3d()
        ylim = self._ax.get_ylim3d()
        zlim = self._ax.get_zlim3d()
        cx = sum(xlim) / 2
        cy = sum(ylim) / 2
        cz = sum(zlim) / 2
        rx = (xlim[1] - xlim[0]) / 2
        ry = (ylim[1] - ylim[0]) / 2
        rz = (zlim[1] - zlim[0]) / 2
        self._ax.set_xlim3d([cx - rx * factor, cx + rx * factor])
        self._ax.set_ylim3d([cy - ry * factor, cy + ry * factor])
        self._ax.set_zlim3d([cz - rz * factor, cz + rz * factor])
        self._canvas.draw_idle()

    def _do_convert(self):
        if self._unique_verts is None:
            messagebox.showwarning("Not Loaded", "Load an STL file first.")
            return

        try:
            scale = float(self.scale_str.get())
        except ValueError:
            messagebox.showerror("Invalid Scale", "Scale must be a number (e.g. 1.0).")
            return

        out = self.output_path.get().strip()
        if not out:
            out = filedialog.asksaveasfilename(
                title="Save SCAD File",
                defaultextension=".scad",
                filetypes=[("OpenSCAD Files", "*.scad"), ("All Files", "*.*")])
            if not out:
                return
            self.output_path.set(out)

        self._btn_convert.config(state="disabled")
        self._btn_load.config(state="disabled")
        self._progress["value"] = 0
        self._set_status("Converting...", C["warn"])
        self._log_msg("Starting conversion...", "info")

        params = dict(
            unique_verts = self._unique_verts,
            faces        = self._faces,
            stats        = self._stats,
            module_name  = self.module_name.get().strip() or "shape",
            precision    = self.precision.get(),
            convexity    = self.convexity.get(),
            scale_factor = scale,
            center       = self.center.get(),
            add_call     = self.add_call.get(),
            add_header   = self.add_header.get(),
            winding_flip = self.winding.get(),
        )
        out_path = out

        def _progress(v):
            self.after(0, lambda: self._progress.configure(value=v))

        def _do():
            try:
                t0   = time.time()
                scad = convert_to_scad(**params, progress_cb=_progress)
                with open(out_path, "w", encoding="utf-8") as fh:
                    fh.write(scad)
                elapsed = time.time() - t0
                self.after(0, lambda: self._on_convert_done(scad, out_path, elapsed))
            except Exception as ex:
                self.after(0, lambda e=ex: self._on_convert_error(e))

        threading.Thread(target=_do, daemon=True).start()

    def _on_convert_done(self, scad: str, path: str, elapsed: float):
        lines   = scad.count("\n") + 1
        size_kb = len(scad.encode()) / 1024

        self._scad_text.config(state="normal")
        self._scad_text.delete("1.0", "end")
        self._scad_text.insert("1.0", scad)
        self._scad_text.config(state="disabled")
        self._scad_line_lbl.config(text=f"{lines:,} lines  |  {size_kb:.1f} KB")

        self._btn_convert.config(state="normal")
        self._btn_load.config(state="normal")
        self._progress["value"] = 100

        self._set_status(
            f"Done in {elapsed:.2f}s  ->  {path}  ({size_kb:.1f} KB)", C["accent2"])
        self._log_msg(
            f"Saved: {path}  [{lines:,} lines, {size_kb:.1f} KB, {elapsed:.2f}s]", "ok")

        self._notebook.select(1)

    def _on_convert_error(self, ex):
        self._btn_convert.config(state="normal")
        self._btn_load.config(state="normal")
        self._set_status(f"Conversion failed: {ex}", C["error"])
        self._log_msg(f"ERROR: {ex}", "error")
        messagebox.showerror("Conversion Error", str(ex))

    def _copy_scad(self):
        content = self._scad_text.get("1.0", "end")
        self.clipboard_clear()
        self.clipboard_append(content)
        self._set_status("Copied to clipboard.", C["accent"])

    def _save_scad(self):
        out = self.output_path.get().strip()
        if not out:
            out = filedialog.asksaveasfilename(
                defaultextension=".scad",
                filetypes=[("OpenSCAD Files", "*.scad")])
        if not out:
            return
        content = self._scad_text.get("1.0", "end")
        with open(out, "w", encoding="utf-8") as fh:
            fh.write(content)
        self._log_msg(f"Saved: {out}", "ok")
        self._set_status(f"Saved: {out}", C["accent2"])


