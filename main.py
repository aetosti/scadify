def load_stl(path: str):
    m = mesh.Mesh.from_file(path)
    verts = m.vectors.reshape(-1, 3)
    unique_verts, inverse = np.unique(verts, axis=0, return_inverse=True)
    faces = inverse.reshape(-1, 3)
    bounds = {
        "x": (float(unique_verts[:, 0].min()), float(unique_verts[:, 0].max())),
        "y": (float(unique_verts[:, 1].min()), float(unique_verts[:, 1].max())),
        "z": (float(unique_verts[:, 2].min()), float(unique_verts[:, 2].max())),
    }
    size = {k: v[1] - v[0] for k, v in bounds.items()}
    stats = {
        "triangles":    len(m.vectors),
        "unique_verts": len(unique_verts),
        "faces":        len(faces),
        "bounds":       bounds,
        "size":         size,
        "file_size_kb": os.path.getsize(path) / 1024,
    }
    return m, unique_verts, faces, stats


def convert_to_scad(
    unique_verts, faces, stats,
    module_name:  str   = "shape",
    precision:    int   = 4,
    convexity:    int   = 10,
    scale_factor: float = 1.0,
    center:       bool  = False,
    add_call:     bool  = True,
    add_header:   bool  = True,
    winding_flip: bool  = False,
    progress_cb=None,
) -> str:
    lines = []

    if add_header:
        lines += [
            "// Generated by STL to SCAD Converter",
            f"// Triangles : {stats['triangles']}",
            f"// Vertices  : {stats['unique_verts']}",
            f"// Size X    : {stats['size']['x']:.4f}",
            f"// Size Y    : {stats['size']['y']:.4f}",
            f"// Size Z    : {stats['size']['z']:.4f}",
            "",
        ]

    verts = unique_verts.copy()

    if center:
        cx = (stats["bounds"]["x"][0] + stats["bounds"]["x"][1]) / 2
        cy = (stats["bounds"]["y"][0] + stats["bounds"]["y"][1]) / 2
        cz = (stats["bounds"]["z"][0] + stats["bounds"]["z"][1]) / 2
        verts[:, 0] -= cx
        verts[:, 1] -= cy
        verts[:, 2] -= cz

    if scale_factor != 1.0:
        verts = verts * scale_factor

    fmt = f"{{:.{precision}f}}"

    lines.append(f"module {module_name}() {{")
    lines.append("    polyhedron(")
    lines.append("        points = [")

    n_verts = len(verts)
    for i, v in enumerate(verts):
        x = fmt.format(v[0])
        y = fmt.format(v[1])
        z = fmt.format(v[2])
        lines.append(f"            [{x}, {y}, {z}],")
        if progress_cb and i % 5000 == 0:
            progress_cb(i / (n_verts + len(faces)) * 50)

    lines.append("        ],")
    lines.append("        faces = [")

    n_faces = len(faces)
    for i, f in enumerate(faces):
        if winding_flip:
            lines.append(f"            [{f[2]}, {f[1]}, {f[0]}],")
        else:
            lines.append(f"            [{f[0]}, {f[1]}, {f[2]}],")
        if progress_cb and i % 5000 == 0:
            progress_cb(50 + i / n_faces * 48)

    lines.append("        ],")
    lines.append(f"        convexity = {convexity}")
    lines.append("    );")
    lines.append("}")

    if add_call:
        lines += ["", f"{module_name}();"]

    if progress_cb:
        progress_cb(100)

    return "\n".join(lines)


