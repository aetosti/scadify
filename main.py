def make_label(parent, text, dim=False, head=False, **kw):
    font  = FONT_HEAD if head else FONT_LABEL
    color = C["text_dim"] if dim else C["text"]
    return tk.Label(parent, text=text, bg=C["panel"], fg=color, font=font, **kw)


def make_entry(parent, textvariable=None, width=40, **kw):
    return tk.Entry(parent, textvariable=textvariable, width=width,
                    bg=C["entry_bg"], fg=C["text"],
                    insertbackground=C["text"],
                    relief="flat", bd=1,
                    highlightthickness=1,
                    highlightbackground=C["border"],
                    highlightcolor=C["accent"],
                    font=FONT_MONO, **kw)


def make_button(parent, text, command, accent=False, danger=False, width=None, **kw):
    bg = C["accent"] if accent else (C["error"] if danger else C["btn"])
    fg = C["bg"] if accent else C["text"]
    b = tk.Button(parent, text=text, command=command,
                  bg=bg, fg=fg,
                  activebackground=C["btn_hover"],
                  activeforeground=C["text"],
                  relief="flat", bd=0,
                  padx=14, pady=6,
                  font=FONT_HEAD,
                  cursor="hand2",
                  **({"width": width} if width else {}),
                  **kw)
    def on_enter(e): b.config(bg=C["btn_hover"] if not accent else "#79baff")
    def on_leave(e): b.config(bg=bg)
    b.bind("<Enter>", on_enter)
    b.bind("<Leave>", on_leave)
    return b


def separator(parent, **kw):
    return tk.Frame(parent, bg=C["border"], height=1, **kw)


